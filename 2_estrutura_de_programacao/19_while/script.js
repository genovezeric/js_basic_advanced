// While é um loop, serve para repetir o bloco até ser fechado, no caso esse bloco repete as numerações no console até o x valer 10
let x = 10
while(x > 0) {
    console.log("O x é", x)
    x = x - 1
}

// Esse é o caso invertido do de cima, ele vai diminuir de 10 até o y chegar em 0
let y = 0
while(y <= 10) {
    console.log(y)
    y = y + 1
}
