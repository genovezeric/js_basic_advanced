// Comparações que existem em boolean
// Maior que: >
console.log(5 > 2);
// Menor que: <
console.log(5 < 2);
// Maior ou igual: >=
console.log(5 > 2);
// Menor ou igual: <=
console.log(5 < 2);
// Igual: ==
console.log(5 == 5);
// Diferente: !=
console.log(5 != 2);
// Idêntico: ===
console.log(5.5 === 2);
