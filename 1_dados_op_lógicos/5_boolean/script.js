// Valores booleans são possiveis apenas 2 resultados, True (Verdadeiro) e False (Falso)

console.log(true);
console.log(false);

// type of para confirmar que realmente é boolean
console.log(typeof (true));
console.log(typeof (false));

// Aqui uma confirmação verdadeira que 5 vezes 5 o resultado é 25 deve retornar "true"
console.log(5 * 5 == 25);
console.log(typeof(5 * 5 == 25));

// Aqui uma confirmação falsa de que 10 vezes 5 não é 25
console.log(10 * 5 == 25);
console.log(typeof(10 * 5 == 25));

